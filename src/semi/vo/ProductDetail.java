package semi.vo;

public class ProductDetail {

	private int no;
	
	private int categoryNo;

	private String name;
	
	private int price;
	
	private int point;
	
	private int discount;
	
	private String detail;
	
	private String status;
	
	private String imgPortrait1;
	
	private String imgPortrait2;
	
	private String imgBig;
	
	private String color;
	
	private String size;
	
	private int qty;

	public int getNo() {
		return no;
	}

	public void setNo(int no) {
		this.no = no;
	}

	public int getCategoryNo() {
		return categoryNo;
	}

	public void setCategoryNo(int categoryNo) {
		this.categoryNo = categoryNo;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	public int getPoint() {
		return point;
	}

	public void setPoint(int point) {
		this.point = point;
	}

	public int getDiscount() {
		return discount;
	}

	public void setDiscount(int discount) {
		this.discount = discount;
	}

	public String getDetail() {
		return detail;
	}

	public void setDetail(String detail) {
		this.detail = detail;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getImgPortrait1() {
		return imgPortrait1;
	}

	public void setImgPortrait1(String imgPortrait1) {
		this.imgPortrait1 = imgPortrait1;
	}

	public String getImgPortrait2() {
		return imgPortrait2;
	}

	public void setImgPortrait2(String imgPortrait2) {
		this.imgPortrait2 = imgPortrait2;
	}

	public String getImgBig() {
		return imgBig;
	}

	public void setImgBig(String imgBig) {
		this.imgBig = imgBig;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

	public String getSize() {
		return size;
	}

	public void setSize(String size) {
		this.size = size;
	}

	public int getQty() {
		return qty;
	}

	public void setQty(int qty) {
		this.qty = qty;
	}

}
